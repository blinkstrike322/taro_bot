# core/utils.py
import re
from typing import List

# Расширенный список слов, часто встречающихся в вопросах о будущем, чувствах, действиях и событиях
_KEYWORDS: List[str] = [
    "как", "когда", "почему", "что", "будет", "смогу", "станет", "поможет", "успею", "получу",
    "какой", "какая", "какие", "кто", "куда", "почему-то", "почему-то", "правильно", "нужно",
    "сделать", "совет", "помощь", "рекомендация", "выйдет", "должен", "получится", "можно",
    "возможно", "хочу", "нравится", "нравится ли", "следует", "следует ли", "влияние", "значение",
    "должно", "готов", "готов ли", "решение", "решать", "как лучше", "удастся", "лучше", "стоит",
    "опасность", "риск", "влияние", "отношение", "чувства", "любовь", "финансы", "работа", "карьера",
    "учёба", "здоровье", "деньги", "богатство", "семья", "друзья", "путешествие", "отдых", "событие",
    "планы", "новости", "возможность", "ситуация", "выбор", "поступок", "поступить", "успех", "провал",
    "события", "перемены", "перемен", "начало", "конец", "поступление", "шанс", "вероятность", "удача",
    "судьба", "карма", "цель", "стремление", "достижение", "любимый", "дорога", "путь", "попробовать",
    "испытание", "вызов", "ожидание", "вопрос", "задача", "труд", "усилия", "награда", "победа", "поражение"
]

def is_valid_question(text: str) -> bool:
    """
    Определяет, является ли текст вопросом или запросом на расклад.
    Убираем проверку на знак вопроса, ориентируемся только на ключевые слова.
    """
    if not text or len(text.strip()) < 3:
        return False

    s = text.lower()
    # Считаем текст валидным, если в нём встречается хотя бы одно ключевое слово
    for word in _KEYWORDS:
        if re.search(r"\b" + re.escape(word) + r"\b", s):
            return True

    # На случай коротких/общих вопросов (например "Любовь", "Работа") считаем валидным
    if len(s.split()) <= 2 and s.isalpha():
        return True

    return False

